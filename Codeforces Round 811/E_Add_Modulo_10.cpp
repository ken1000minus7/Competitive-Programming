        {
            cin>>arr[i];
            if(arr[i]%2!=0)
            {
                arr[i] += arr[i]%10;
            }
            maxi = max(maxi,arr[i]);
        }
        bool op = true;
        for(ll &x : arr)
        {
            ll diff = maxi - x;
            ll loops = diff/20;
            ll start = x%10;
            if(start==0)
            {
                if(x==maxi) continue;
                op = false;
                break;
            }
            x += 20*loops;
            
            int c = 0;
            while(x < maxi && c<4)
            {
                if(start + x <=maxi)
                {
                    x += start;
                    start = x%10;
                }
                else break;
                c++;
            }
            if(x!=maxi)
            {
                op= false;
                break;
            }
        }
