pair<ll,ll> getConfig(int i,vector<ll> &arr,vector<pair<ll,ll>> &dp)
{
    if(i<=0 || i>=arr.size()-1) return {0,0};
    if(dp[i].first!=-1) return dp[i];
    dp[i] = {0,0};
    pair<ll,ll> p1 = getConfig(i+2,arr,dp);
    pair<ll,ll> p2 = getConfig(i+1,arr,dp);
    ll diff = max(arr[i-1]-arr[i],arr[i+1]-arr[i]);
    pair<ll,ll> p3 = {p1.first+1,p1.second + max((ll)0,diff+1)};
    // cout<<i<<"\n";
    // cout<<p1.first<<" "<<p1.second<<" "<<"\n";
    // cout<<p2.first<<" "<<p2.second<<" "<<"\n";
    // cout<<p3.first<<" "<<p3.second<<" "<<"\n";
    if(p2.first>p3.first)
    {
        dp[i]=p2;
    }
    else if(p3.first>p2.first)
    {
        dp[i]=p3;
    }
    else if(p3.second<p2.second) 
    {
        dp[i] = p3;
    }
    else dp[i] = p2;
    return dp[i];
}

