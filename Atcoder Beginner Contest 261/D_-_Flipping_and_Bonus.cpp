ll getAns(int i,vector<ll> &x,vector<ll> &y,vector<vector<ll>> &dp,int c)
{
    if(i>=x.size()) return 0;
    if(dp[i][c]!=-1) return dp[i][c];
    return dp[i][c] = max(x[i]+y[c+1]+getAns(i+1,x,y,dp,c+1),getAns(i+1,x,y,dp,0));
}

