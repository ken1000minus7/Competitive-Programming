vector<int> maxiIndices(vector<ll> arr) {
    int n = arr.size();
    vector<int> ans;
    for(int _ = 0; _ < 3; _++) {
        ll maxi = 0;
        int ind = 0;
        for(int i = 0; i < n; i++) {
            if(arr[i] > maxi) {
                maxi = arr[i];
                ind = i;
            }
        }
        ans.push_back(ind);
        arr[ind] = INT32_MIN; 
    }
    return ans;
}

