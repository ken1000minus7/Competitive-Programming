int maxiInd(vector<ll> &arr, vector<ll> &sarr, vector<int> &done) {
    int ans = -1;
    for(int i = 0; i < arr.size(); i++) {
        if(done[i]) continue;
        if(ans == -1 || arr[ans] <= arr[i]) {
            if(ans != -1 && arr[ans] == arr[i]) {
                if(sarr[i] >= sarr[ans]) ans = i;
            } else ans = i;
        }
    }
    return ans;
}

ll dostuff(int i, vector<ll> &a, vector<ll> &b, vector<int> &done) {
    if(i >= done.size()) return 0;
    ll ans = (i % 2 == 0) ? INT64_MIN : INT64_MAX;
    for(int j = 0; j < done.size(); j++) {
        if(done[j]) continue;
        done[j]++;
        ll mcontri = (i % 2 == 0) ? a[j] - 1 : 1 - b[j];
        ll contri = dostuff(i + 1, a, b, done) + mcontri;
        ans = (i % 2 == 0) ? max(ans, contri) : min(ans, contri);
        done[j]--;
    }
    return ans;
}

