int main()
{
    set<int> nums = {4, 8, 15, 16, 23, 42};
    unordered_map<int, set<int>> m;
    for(int a : nums) {
        for(int b : nums) {
            if(a == b) continue;
            m[a * b] = {a, b};
        }
    }
    cout << "? 1 1\n";
    cout.flush();
    
    int a0, a1;
    cin >> a0;

    cout << "? 2 2\n";
    cout.flush();
    cin >> a1;
    a0 = sqrt(a0);
    a1 = sqrt(a1);
    vector<int> ans = {a0, a1};
    nums.erase(a0);
    nums.erase(a1);
    
    int p1, p2;
    cout << "? 3 4\n";
    cout.flush();
    cin >> p1;
    cout << "? 4 5\n";
    cout.flush();
    cin >> p2;

    set<int> s1 = m[p1], s2 = m[p2];

    int mid = 0;
    for(int a : s1) {
        if(s2.count(a)) {
            mid = a;
            break;
        }
    }
    s1.erase(mid);
    s2.erase(mid);
    int left = *s1.begin();
    int right = *s2.begin();
    nums.erase(left);
    nums.erase(mid);
    nums.erase(right);
    ans.push_back(left);
    ans.push_back(mid);
    ans.push_back(right);
    ans.push_back(*nums.begin());

    cout << "! ";
    for(int a : ans) {
        cout << a << " ";
    }
    cout << "\n";

    return 0;
}