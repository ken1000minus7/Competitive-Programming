ll mod = 998244353;

long long moduloMultiplication(long long a, long long b,
                               long long mod)
{
 
    // Initialize result
    long long res = 0;
 
    // Update a if it is more than
    // or equal to mod
    a %= mod;
 
    while (b) {
 
        // If b is odd, add a with result
        if (b & 1)
            res = (res + a) % mod;
 
        // Here we assume that doing 2*a
        // doesn't cause overflow
        a = (2 * a) % mod;
        b >>= 1; // b = b / 2
    }
    return res;
}
 
// C++ function for extended Euclidean Algorithm
long long int gcdExtended(long long int a, long long int b,
                          long long int* x,
                          long long int* y);
 
// Function to find modulo inverse of b. It returns
// -1 when inverse doesn't exists
long long int modInverse(long long int b, long long int m)
{
 
    long long int x, y; // used in extended GCD algorithm
    long long int g = gcdExtended(b, m, &x, &y);
 
    // Return -1 if b and m are not co-prime
    if (g != 1)
        return -1;
 
    // m is added to handle negative x
    return (x % m + m) % m;
}
 
// C function for extended Euclidean Algorithm (used to
// find modular inverse.
long long int gcdExtended(long long int a, long long int b,
                          long long int* x,
                          long long int* y)
{
 
    // Base Case
    if (a == 0) {
        *x = 0, *y = 1;
        return b;
    }
 
    // To store results of recursive call
    long long int x1, y1;
 
    long long int gcd = gcdExtended(b % a, a, &x1, &y1);
 
    // Update x and y using results of recursive
    // call
    *x = y1 - (b / a) * x1;
    *y = x1;
    return gcd;
}
 
long long int modDivide(long long int a, long long int b,long long int m)
{
    a = a % m;
    long long int inv = modInverse(b, m);
    if (inv == -1)
        return 0;
    else
        return (inv * a) % m;
}
ll nCr(ll n, ll r)
{
    if (r > n)
        return 0;

    if (r > n - r)
        r = n - r;
 
    ll x = 1;
    for (int i = 1; i <= r; i++) {
        x = moduloMultiplication(x, (n + 1 - i), mod);
        x = modDivide(x, i, mod);
    }
    return x;
}

ll getAns(ll n,bool win)
{
    if(win)
    {
        if(n<=2) return 1;
        ll rem = n/2 - 1;
        return (nCr(n-1,rem)%mod + getAns(n-2,false)%mod)%mod;
    }
    else
    {
        if(n<=2) return 0;
        ll rem = n/2 - 2;
        if(rem<0) return 0;
        return (nCr(n-2,rem)%mod + getAns(n-2,true)%mod)%mod;
    }
}

