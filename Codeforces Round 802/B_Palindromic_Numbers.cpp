string diff(string s1,string s2)
{
    string ans = "";
    int mini = 0;
    int i = s2.length()-1;
    int j = s1.length() - 1;
    while(i>=0 || j>=0)
    {
        int n1 = (i>=0) ? s2[i] : 0;
        int n2 = (j>=0) ? s1[j] : 0;
        int sub = n2 - n1 - mini;
        if(sub<0) sub+=10,mini=1;
        else mini = 0;
        if(i>=0) ans+= (sub + '0');
        i--,j--;
    }
    reverse(ans.begin(),ans.end());
    
    return ans;
}

