class node{
    public:
    int val;
    int ltree,rtree;
    node * left,*right;

    node(int val)
    {
        this->val = val;
        left = NULL;
        right = NULL;
    }

    int subtreeSize()
    {
        return ltree+rtree;
    }
    int subtreeDiff()
    {
        return abs(ltree-rtree);
    }
};

int computeSizes(node * n)
{
    if(n==NULL) return 0;
    n->ltree = computeSizes(n->left);
    n->rtree = computeSizes(n->right);
    return 1 + n->ltree + n->rtree;
}

node* getNode(int i,vector<vector<int>> &adj,vector<int> &done)
{
    done[i]++;
    node * n = new node(i);
    for(int j=0;j<adj[i].size();j++)
    {
        if(!done[adj[i][j]])
        {
            if(n->left==NULL) n->left = getNode(adj[i][j],adj,done);
            else n->right = getNode(adj[i][j],adj,done);
        }
    }
    return n;
}

int doStuff(node * temp)
{
    if(temp==NULL) return 0;
    if(temp->left==NULL && temp->right==NULL) return 0;
    if(temp->right==NULL)
    {
        return temp->left->ltree + temp->left->rtree;
    }
    if(temp->left==NULL)
    {
        return temp->right->ltree + temp->right->rtree;
    }
    int c1 = doStuff(temp->left) + temp->right->subtreeSize();
    int c2 = doStuff(temp->right) + temp->left->subtreeSize();
    return max(c1,c2);
}

