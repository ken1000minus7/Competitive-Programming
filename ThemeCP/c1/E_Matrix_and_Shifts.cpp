void dostuff(int si, int sj, vector<vector<int>> &arr, int &ones, int ops, int &ans, vector<vector<int>> &done) {
    if(ops == 0 || done[si][sj]) return;
    int n = arr.size(); 
    done[si][sj]++;

    dostuff(si, (sj + 1) % n, arr, ones, ops - 1, ans, done);
    dostuff(si, (sj - 1 + n) %n, arr, ones, ops - 1, ans, done);
    dostuff((si + 1) % n, sj, arr, ones, ops - 1, ans, done);
    dostuff((si - 1 + n) % n, sj, arr, ones, ops - 1, ans, done);

    int dones = 0;
    for(int _ = 0; _ < n; _++) {
        dones += arr[si][sj];
        si = (si + 1) % n;
        sj = (sj + 1) % n;
    }
    ans = min(ans, (n - dones) + (ones - dones));
}

