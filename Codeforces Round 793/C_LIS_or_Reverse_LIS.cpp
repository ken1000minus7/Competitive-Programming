int lis(int i,int arr[],vector<int> &dp)
{
    if(dp[i]!=-1) return dp[i];
    int ans = 1;
    for(int j=0;j<i;j++)
    {
        if(arr[j]<arr[i]) ans = max(ans,1+lis(j,arr,dp));
    }
    dp[i] = ans;
    return dp[i];
}

