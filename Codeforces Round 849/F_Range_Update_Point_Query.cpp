int getdigsum(ll n) {
    ll ans = 0;
    while(n > 0) {
        ans += n % 10;
        n /= 10;
    }
    return ans;
}

void handleLazy(vector<int> &lazy,vector<ll> &segTree,int &l,int &r,int &ind)
{
    ll temp = segTree[ind];
    for(int i = 0; i < lazy[ind]; i++) {
        if(temp < 10) break;
        temp = getdigsum(temp);
    }
    segTree[ind] = temp;
    if(l<r)
    {
        lazy[2*ind+1] += lazy[ind];
        lazy[2*ind+2] += lazy[ind];
    }
    lazy[ind]=0;
}

void buildTree(vector<ll> &arr,vector<ll> &segTree,int l,int r,int ind)
{
    if(l==r)
    {
        segTree[ind] = arr[l];
        return;
    }
    int mid = l + (r-l)/2;
    buildTree(arr,segTree,l,mid,2*ind + 1);
    buildTree(arr,segTree,mid+1,r,2*ind+2);
    // segTree[ind] = segTree[2*ind+1] + segTree[2*ind+2];
}

void updateRange(vector<int> &lazy,vector<ll> &segTree,int l,int r,int ind,int &s,int &e,int val)
{
    handleLazy(lazy,segTree,l,r,ind);
    if(l>=s && r<=e) 
    {
        lazy[ind]+=val;
        handleLazy(lazy,segTree,l,r,ind);
        return;
    }
    else if(l>e || r<s) return;
    else 
    {
        int mid = l + (r-l)/2;
        updateRange(lazy,segTree,l,mid,2*ind+1,s,e,val);
        updateRange(lazy,segTree,mid+1,r,2*ind+2,s,e,val);
        // segTree[ind] = segTree[2*ind+1] + segTree[2*ind+2];
    }
}

int query(vector<int> &lazy,vector<ll> &segTree,int l,int r,int &s,int &e,int ind)
{
    handleLazy(lazy,segTree,l,r,ind);
    if(l>=s && r<=e) return segTree[ind];
    else if(l>e || r<s) return 0;
    else 
    {
        int mid = l + (r-l)/2;
        return query(lazy,segTree,l,mid,s,e,2*ind+1) + query(lazy,segTree,mid+1,r,s,e,2*ind+2);
    }
}

