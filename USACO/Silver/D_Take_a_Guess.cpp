int main()
{
    int n, k;
    cin >> n >> k;

    auto query = [&](int i, int j, int type) {
        string s = type == 0 ? "or" : "and";
        cout << s << " " << i << " " << j << "\n";
        cout.flush();
    };
    auto sum = [&](int i, int j) {
        query(i, j, 0);
        ll o, a;
        cin >> o;

        query(i, j, 1);
        cin >> a;

        ll x = (~a) & o;
        return (2 * a) + x;
    };
    

    ll a1a2 = sum(1, 2);
    ll a2a3 = sum(2, 3);
    ll a1a3 = sum(1, 3);

    ll a3 = (a1a3 + a2a3 - a1a2) / 2;
    ll a1 = a1a3 - a3;
    ll a2 = a1a2 - a1;

    vector<ll> arr = {a1, a2, a3};
    ll prev = a3;
    for(int i = 4; i <= n; i++) {
        ll s = sum(i - 1, i);
        prev = s - prev;
        arr.push_back(prev);
    }
    sort(all(arr));
    cout << "finish " << arr[k - 1] << "\n";
    return 0;
}