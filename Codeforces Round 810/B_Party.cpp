int dfs(int i,vector<vector<int>> &adj,vector<int> &done,vector<int> &arr)
{
    if(done[i]) return INT32_MAX;
    done[i]++;
    int ans = arr[i];
    int mini = INT32_MAX;
    for(int j : adj[i])
    {
        // cout<<j<<" "<<arr[j]<<"\n";
        if(adj[j].size()%2==0) mini = min(mini,arr[j]);
    }
    if(mini==INT32_MAX) return mini;
    // cout<<ans<<" lol "<<mini<<"\n";
    ans += mini;
    return ans;
}

