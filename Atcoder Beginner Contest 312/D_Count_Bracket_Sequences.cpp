ll dostuff(int i, int open, string &s, vector<vector<ll>> &dp) {
    if(open < (i - open)) return 0;
    if(i >= s.length()) {
        if(open == (i - open)) {
            return 1;
        }
        else {
            return 0;
        }
    }
    if(dp[i][open] != -1) return dp[i][open];
    if(s[i] == '?') {
        return dp[i][open] = (dostuff(i + 1, open, s, dp) % mod + dostuff(i + 1, open + 1, s, dp) % mod) % mod;
    }
    else {
        return dp[i][open] = dostuff(i + 1, open + (s[i] == '('), s, dp) % mod;
    }
}

