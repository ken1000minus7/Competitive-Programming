ll mod = 998244353;

ll power(ll a, ll b) {
    ll res = 1;
    while (b > 0) {
        if (b & 1) {
            res = (res * a) % mod;
        }
        a = (a * a) % mod;
        b >>= 1;
    }
    return res;
}

ll ncr(ll n, ll r) {
    if(r > n - r) r = n - r;
    ll numerator = 1, denominator = 1;
    for(ll i = 0; i < r; i++) {
        numerator = (numerator * (n - i)) % mod;
        denominator = (denominator * (i + 1)) % mod;
    }
    ll inv = power(denominator, mod - 2);
    return (numerator * inv) % mod;
}

