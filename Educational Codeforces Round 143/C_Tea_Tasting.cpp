        buildTree(ans, segTree, 0, n - 1, 0);
        for(int i = 0; i < n; i++) {
            ll target = a[i] + (i > 0 ? prefsum[i - 1] : 0);
            int ind = upper_bound(all(prefsum), target) - prefsum.begin();
            updateRange(lazy, segTree, 0, n - 1, 0, i, ind - 1, 1LL);
            if(ind < n) {
                ll drunk = (ind > 0 ? prefsum[ind - 1] : 0) - (i > 0 && i <= ind ? prefsum[i - 1] : 0);
                ans[ind] += a[i] - drunk;
            }
        }
        for(int i = 0; i < n; i++) {
            ans[i] += b[i] * query(lazy, segTree, 0, n - 1, i, i, 0);
        }
