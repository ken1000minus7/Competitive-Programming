void dostuff(int s,vector<vector<int>> &grid,int &ans)
{
    if(s>=grid.size()/2) return;
    for(int i=s;i<grid.size()-s-1;i++)
    {
        int o = 0, z = 0;

        if(grid[s][i]==0) z++;
        else o++;

        if(grid[i][grid.size()-s-1]==0) z++;
        else o++;

        if(grid[grid.size()-s-1][grid.size()-i-1]==0) z++;
        else o++;

        if(grid[grid.size()-i-1][s]==0) z++;
        else o++;

        ans+=min(o,z);
    }
    dostuff(s+1,grid,ans);
}

