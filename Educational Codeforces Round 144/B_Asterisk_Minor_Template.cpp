string longestCommonSubstring(string s1, string s2) {
    int n1 = s1.length(), n2 = s2.length();
    int dp[n1+1][n2+1], maxLength = 0, endIndex = 0;
    memset(dp, 0, sizeof(dp));
    
    for (int i = 1; i <= n1; ++i) {
        for (int j = 1; j <= n2; ++j) {
            if (s1[i-1] == s2[j-1]) {
                dp[i][j] = dp[i-1][j-1] + 1;
                if (dp[i][j] > maxLength) {
                    maxLength = dp[i][j];
                    endIndex = i-1;
                }
            }
        }
    }
    
    string lcs = "";
    while (maxLength > 0) {
        lcs = s1[endIndex] + lcs;
        --maxLength;
        --endIndex;
    }
    return lcs;
}

