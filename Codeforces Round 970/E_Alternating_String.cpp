        int n;
        cin >> n;
        string s;
        cin >> s;
        vector<int> freq(26);
        vector<vector<int>> evefreq, oddfreq;
        for(int i = 0; i < n; i += 2) {
            freq[s[i] - 'a']++;
            evefreq.push_back(freq);
            evefreq.push_back(freq);
        }
        freq = vector<int>(26);
        oddfreq.push_back(freq);
        for(int i = 1; i < n; i += 2) {
            freq[s[i] - 'a']++;
            oddfreq.push_back(freq);
            oddfreq.push_back(freq);
        }
        if(n % 2 == 0) {
            int maxi1 = 0, maxi2 = 0;
            for(int a : evefreq[evefreq.size() - 1]) maxi1 = max(maxi1, a);
            for(int a : oddfreq[oddfreq.size() - 1]) maxi2 = max(maxi2, a);
            cout << (n - maxi1 - maxi2) << "\n";
            continue;
        }
        int ans = INT32_MAX;

        // for(int i = 0; i < evefreq.size(); i++) {
        //     for(int j = 0; j < 26; j++) {
