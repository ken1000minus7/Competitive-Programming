        vector<string> arr(n);
        vector<vector<ll>> sumi(100, vector<ll>(100));
        vector<ll> sums(n);
        for(int i = 0; i < n; i++) {
            cin >> arr[i];
            ll sum = 0;
            for(auto a : arr[i]) sum += (a - '0');
            sumi[sum][arr[i].length()]++;
            sums[i] = sum;
            // cout << sum << " " << arr[i] << "\n";
        }
        ll ans = 0;
        for(int i = 0; i < n; i++) {
            ll front = 0, back = 0;;
            ll curr = 0;
            for(int j = 0; j < arr[i].length() - 1; j++) {
                curr += (arr[i][j] - '0');
                ll req = curr - (sums[i] - curr);
                int leni = 2 * (j + 1) - arr[i].length();
                if(req <= 0 || leni <= 0) continue;
                // cout << curr << " " << req << " " << i << "\n";
                front += sumi[req][leni];
            }
            curr = 0;
            for(int j = arr[i].length() - 1; j > 0; j--) {
                curr += (arr[i][j] - '0');
                ll req = curr - (sums[i] - curr);
                int leni = 2 * (arr[i].length() - j) - arr[i].length();
                if(req <= 0 || leni <= 0) continue;
                // cout << curr << " " << req << " " << i << "\n";
                back += sumi[req][leni];
            }
            ll mans = front + back;
            ans += mans;
            // cout << front << " " << back << " lol " << mans << "\n";
        }
        vector<vector<ll>> eqs(100, vector<ll>(100));
        for(int i = 0; i < n; i++) {
            eqs[sums[i]][arr[i].length()]++;
            ans += (2 * eqs[sums[i]][arr[i].length()]) - 1;
        }
        cout << ans << "\n";
