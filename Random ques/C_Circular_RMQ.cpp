void buildTree(vector<ll> &arr,vector<ll> &segTree,int l,int r,int ind)
{
    if(l==r)
    {
        segTree[ind] = arr[l];
        return;
    }
    int mid = l + (r-l)/2;
    buildTree(arr,segTree,l,mid,2*ind+1);
    buildTree(arr,segTree,mid+1,r,2*ind+2);
    auto left = segTree[2*ind+1];
    auto right = segTree[2*ind+2];
    segTree[ind] = min(left,right);
}

void update(vector<ll> &segTree,vector<ll> &lazy,int l,int r,int ind,int s,int e,ll &x)
{
    segTree[ind] += lazy[ind];
    if(l!=r)
    {
        lazy[2*ind+1] += lazy[ind];
        lazy[2*ind+2] += lazy[ind];
    }
    lazy[ind] = 0;
    if(l>=s && r<=e)
    {
        segTree[ind] += x;
        if(l!=r)
        {
            lazy[2*ind+1] += x;
            lazy[2*ind+2] += x;
        }
        return;
    }
    if(r<s || l>e) return;
    int mid = l + (r-l)/2;
    update(segTree,lazy,l,mid,2*ind+1,s,e,x);
    update(segTree,lazy,mid+1,r,2*ind+2,s,e,x);
    auto left = segTree[2*ind+1];
    auto right = segTree[2*ind+2];
    segTree[ind] = min(left,right);
}

ll query(vector<ll> &segTree,vector<ll> &lazy,int l,int r,int ind,int s,int e)
{
    segTree[ind] += lazy[ind];
    if(l!=r)
    {
        lazy[2*ind+1] += lazy[ind];
        lazy[2*ind+2] += lazy[ind];
    }
    lazy[ind] = 0;
    if(l>=s && r<=e)
    {
        return segTree[ind];
    }
    if(r<s || l>e) return INT64_MAX;
    int mid = l + (r-l)/2;
    auto left = query(segTree,lazy,l,mid,2*ind+1,s,e);
    auto right = query(segTree,lazy,mid+1,r,2*ind+2,s,e);
    return min(left,right);
}

