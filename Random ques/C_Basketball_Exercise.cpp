    auto stuff = [&](auto &&self, int i, int j) -> ll {
        if(j >= n) return 0;
        if(dp[i][j] != -1) return dp[i][j];
        return dp[i][j] = max(self(self, 1 - i, j + 1) + arr[1 - i][j], self(self, i, j + 1));
    };

