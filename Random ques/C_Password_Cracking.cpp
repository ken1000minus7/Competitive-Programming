int main()
{
    int t;
    cin >> t;
    while(t--) {
        int n;
        cin >> n;
        string ans = "";
        bool back = false;
        for(int i = 0; i < n; i++) {
            if(!back) {
                cout << "? " << ans << "0\n";
                cout.flush();
                int z, o;
                cin >> z;
                if(z) {
                    ans += "0";
                    continue;
                }
                cout << "? " << ans << "1\n";
                cout.flush();
                cin >> o;

                if(o) {
                    ans += "1";
                    continue;
                }
                back = true;
            }
            if(back) {
                cout << "? 0" << ans << "\n";
                cout.flush();
                int z;
                cin >> z;
                if(z) {
                    ans = "0" + ans;
                } 
                else {
                    ans = "1" + ans;
                }
            }
        }
        cout << "! " << ans << "\n";
        cout.flush();
    }
    return 0;
}