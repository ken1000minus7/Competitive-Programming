void buildTree(vector<ll> &arr,vector<ll> &segTree,int l,int r,int ind)
{
    if(l==r)
    {
        segTree[ind] = arr[l];
        return;
    }
    int mid = l + (r-l)/2;
    buildTree(arr,segTree,l,mid,2*ind+1);
    buildTree(arr,segTree,mid+1,r,2*ind+2);
    int p = log2(r-l+1);
    if(p%2!=0)
    {
        segTree[ind] = segTree[2*ind+1] | segTree[2*ind+2];
    }
    else
    {
        segTree[ind] = segTree[2*ind+1] ^ segTree[2*ind+2];
    }
}

void update(vector<ll> &arr,vector<ll> &segTree,int l,int r,int ind,int &i,int &x)
{
    if(l==r)
    {
        segTree[ind] = x;
        arr[l] = x;
        return;
    }
    int mid = l + (r-l)/2;
    if(i<=mid)
    {
        update(arr,segTree,l,mid,2*ind+1,i,x);
    }
    else
    {
        update(arr,segTree,mid+1,r,2*ind+2,i,x);
    }
    int p = log2(r-l+1);
    if(p%2!=0)
    {
        segTree[ind] = segTree[2*ind+1] | segTree[2*ind+2];
    }
    else
    {
        segTree[ind] = segTree[2*ind+1] ^ segTree[2*ind+2];
    }
}

