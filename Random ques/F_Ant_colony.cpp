ll gcd(ll a,ll b)
{
    if(a==0) return b;
    if(b==0) return a;
    return gcd(b,a%b);
}

void buildTree(vector<ll> &arr,vector<pair<ll,int>> &segTree,int l,int r,int ind)
{
    if(l==r)
    {
        segTree[ind] = {arr[l],1};
        return;
    }
    int mid = l + (r-l)/2;
    buildTree(arr,segTree,l,mid,2*ind+1);
    buildTree(arr,segTree,mid+1,r,2*ind+2);
    auto left = segTree[2*ind+1];
    auto right = segTree[2*ind+2];
    ll hcf = gcd(left.first,right.first);
    int count = 0;
    if(left.first==hcf) count+=left.second;
    if(right.first==hcf) count+=right.second;
    segTree[ind] = {hcf,count};
}

pair<ll,int> query(vector<pair<ll,int>> &segTree,int l,int r,int ind,int &s,int &e)
{
    if(l>=s && r<=e) return segTree[ind];
    if(l>e || r<s) return {0,0};
    int mid = l + (r-l)/2;
    auto left = query(segTree,l,mid,2*ind+1,s,e);
    auto right = query(segTree,mid+1,r,2*ind+2,s,e);
    ll hcf = gcd(left.first,right.first);
    int count = 0;
    if(left.first==hcf) count+=left.second;
    if(right.first==hcf) count+=right.second;
    return {hcf,count};
}


