class TrieNode{
public:
    int num;
    bool isTerminal;
    TrieNode* zeroNode, *oneNode;
	ll count;
    TrieNode(int n = 0)
    {
        this->num = n;
        isTerminal = false;
        zeroNode = NULL;
        oneNode = NULL;
		count = 0;
    }
};

class Trie{

public:
    TrieNode* root;

    Trie()
    {
        root = new TrieNode();
    }

    void insert(int n)
    {
        TrieNode* temp = root;
        for(int i=31;i>=0;i--)
        {
            int num = (n>>i) & 1;
            if(num==0){
                if(temp->zeroNode==NULL) temp->zeroNode = new TrieNode(0);
				temp->zeroNode->count++;
                temp = temp->zeroNode;
            }
            else{
                if(temp->oneNode==NULL) temp->oneNode = new TrieNode(1);
				temp->oneNode->count++;
                temp = temp->oneNode;
            }
        }
        temp->isTerminal = true;
    }

};

ll getXor(int i,int &n,int &k,TrieNode* temp)
{
	if(temp==NULL) return 0;
	if(i<0) return 0;
    int ne = (n>>i) & 1;
	int ke = (k>>i) & 1;
	if(ne==0)
	{
		if(ke==0)
		{
			return getXor(i-1,n,k,temp->zeroNode);
		}
		else
		{
			return getXor(i-1,n,k,temp->oneNode) + ((temp->zeroNode!=NULL) ? temp->zeroNode->count : 0);
		}
	}
	else
	{
		if(ke==0)
		{
			return getXor(i-1,n,k,temp->oneNode);
		}
		else
		{
			return getXor(i-1,n,k,temp->zeroNode) + ((temp->oneNode!=NULL) ? temp->oneNode->count : 0);
		}
	}
}

