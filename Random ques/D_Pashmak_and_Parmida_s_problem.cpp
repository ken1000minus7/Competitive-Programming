void update(vector<int> &segTree,int l,int r,int ind,int &i)
{
    if(l==r)
    {
        segTree[ind]++;
        return;
    }
    int mid = l + (r-l)/2;
    if(i<=mid)
    {
        update(segTree,l,mid,2*ind+1,i);
    }
    else
    {
        update(segTree,mid+1,r,2*ind+2,i);
    }
    segTree[ind] = segTree[2*ind+1] + segTree[2*ind+2];
}

int query(vector<int> &segTree,int l,int r,int ind,int s,int e)
{
    if(l>=s && r<=e)
    {
        return segTree[ind];
    }
    if(l>e || r<s) return 0;
    int mid = l + (r-l)/2;
    return query(segTree,l,mid,2*ind+1,s,e) + query(segTree,mid+1,r,2*ind+2,s,e);
}

