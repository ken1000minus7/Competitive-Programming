ll gcd(ll a,ll b)
{
    if(a==0) return b;
    if(b==0) return a;
    return gcd(b,a%b);
}

void buildTree(vector<ll> &arr,vector<ll> &segTree,int l,int r,int ind)
{
    if(l==r)
    {
        segTree[ind] = arr[l];
        return;
    }
    int mid = l + (r-l)/2;
    buildTree(arr,segTree,l,mid,2*ind+1);
    buildTree(arr,segTree,mid+1,r,2*ind+2);
    segTree[ind] = gcd(segTree[2*ind+1],segTree[2*ind + 2]);
}

void update(vector<ll> &arr,vector<ll> &segTree,int l,int r,int ind,int &i,ll &x)
{
    if(l==r)
    {
        segTree[ind] = x;
        arr[l] = x;
        return;
    }
    int mid = l + (r-l)/2;
    if(i<=mid)
    {
        update(arr,segTree,l,mid,2*ind+1,i,x);
    }
    else
    {
        update(arr,segTree,mid+1,r,2*ind+2,i,x);
    }
    segTree[ind] = gcd(segTree[2*ind+1],segTree[2*ind + 2]);
}

ll query(vector<ll> &segTree,int l,int r,int ind,int &s,int &e,ll &x,bool &done)
{
    if(done) return 0;
    if(l>=s && r<=e) 
    {
        if(segTree[ind]%x==0) return 0;
        else if(l==r) return 1;
    }
    if(l>e || r<s) return 0;
    int mid = l + (r-l)/2;
    ll left = query(segTree,l,mid,2*ind+1,s,e,x,done);
    ll right = query(segTree,mid+1,r,2*ind+2,s,e,x,done);
    if(left+right>=2) done=true;
    return left + right;
}

