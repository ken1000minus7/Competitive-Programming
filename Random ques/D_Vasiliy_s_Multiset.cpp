    TrieNode* root = new TrieNode(0);
    insert(root,0);
    while(q--)
    {
        char c;
        cin>>c;
        ll x;
        cin>>x;
        if(c=='+')
        {
            insert(root,x);
        }
        else if(c=='-')
        {
            del(root,x);
        }
        else
        {
            cout<<getXor(root,x)<<"\n";
        }
    }
    return 0;
