pair<int,int> dostuff(vector<vector<int>> &children,string &color,int i,int &ans)
{
    int b =0;
    int w=0;
    if(color[i]=='W') w++;
    else b++;
    for(int j=0;j<children[i].size();j++)
    {
        pair<int,int> child = dostuff(children,color,children[i][j],ans);
        b+=child.first;
        w+=child.second;
    }
    if(b==w) ans++;
    return {b,w};
}

