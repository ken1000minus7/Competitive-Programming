void dostuff(ll &a, ll&b, ll &c, ll &d, ll contria, ll contrib, bool &op) {
    if(op) return;
    ll s1 = ceil((double) (a + 1) / contria) * contria;
    ll s2 = ceil((double) (b + 1) / contrib) * contrib;
    if(s1 <= c && s2 <= d) {
        op = true;
        cout << s1 << " " << s2 << "\n";
    }
}

