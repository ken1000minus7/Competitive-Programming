int power(ll x, unsigned int y, int p)
{
    int res = 1;    
 
    x = x % p; 
  
    if (x == 0) return 0; 
 
    while (y > 0)
    {
        if (y & 1)
            res = (res*x) % p;
        y = y>>1; 
        x = (x*x) % p;
    }
    return res;
}

void buildTree(vector<char> &arr,vector<pair<int,int>> &segTree,int l,int r,int ind)
{
    if(l==r)
    {
        segTree[ind] = {1,arr[l]-'0'};
        return;
    }
    int mid = l + (r-l)/2;
    buildTree(arr,segTree,l,mid,2*ind+1);
    buildTree(arr,segTree,mid+1,r,2*ind+2);
    auto left = segTree[2*ind+1];
    auto right = segTree[2*ind+2];
    int num = ((left.second*power((ll)2,right.first,3))%3+right.second%3) % 3;
    segTree[ind] = {left.first+right.first, num};
}

void update(vector<char> &arr,vector<pair<int,int>> &segTree,int l,int r,int ind,int &i)
{
    if(l==r)
    {
        if(segTree[ind].second==0) 
        {
            segTree[ind] = {1,1};
            arr[i] = '1';
        }
        return;
    }
    int mid = l + (r-l)/2;
    if(i<=mid)
    {
        update(arr,segTree,l,mid,2*ind+1,i);
    }
    else
    {
        update(arr,segTree,mid+1,r,2*ind+2,i);
    }
    auto left = segTree[2*ind+1];
    auto right = segTree[2*ind+2];
    int num = ((left.second*power((ll)2,right.first,3))%3+right.second%3) % 3;
    segTree[ind] = {left.first+right.first, num};
}

pair<int,int> query(vector<pair<int,int>> &segTree,int l,int r,int ind,int &s,int &e)
{
    if(l>=s && r<=e) return segTree[ind];
    if(l>e || r<s) return {0,0};
    int mid = l + (r-l)/2;
    auto left = query(segTree,l,mid,2*ind+1,s,e);
    auto right = query(segTree,mid+1,r,2*ind+2,s,e);
    int num = ((left.second*power((ll)2,right.first,3))%3+right.second%3) % 3;
    return {left.first+right.first, num};
}

