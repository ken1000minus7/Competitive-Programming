    vector<int> parent(n, -1);
    vector<int> rank(n);
    vector<set<int>> langs(n);
    bool nab = true;
    for(int i = 0; i < n; i++) {
        int k;
        cin >> k;
        while(k--) {
            nab = false;
            int a;
            cin >> a;
            langs[i].insert(a);
        }
    }
    for(int i = 0; i < n; i++) {
        for(int j = 0; j < n; j++) {
            if(i == j) continue;
            vector<int> intersec;
            set_intersection(all(langs[i]), all(langs[j]), back_inserter(intersec));
            if(!intersec.empty()) {
                unify(i, j, parent, rank);
            }
        }
    }
    vector<int> done(n);
    int c = 0;
    for(int i = 0; i < n; i++) {
        if(done[find(i, parent)]) continue;
        done[find(i, parent)]++;
        c++;
    }
    if(nab) c++;
    cout << (c - 1) << "\n";
    return 0;
