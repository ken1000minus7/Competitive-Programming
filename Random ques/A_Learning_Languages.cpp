int find(int i, vector<int> &parent) {
    if(parent[i] == -1) return i;
    return parent[i] = find(parent[i], parent);
}

void unify(int i, int j, vector<int> &parent, vector<int> &rank) {
    i = find(i, parent);
    j = find(j, parent);
    if(i == j) return;
    if(rank[i] >= rank[j]) {
        parent[j] = i;
        if(rank[i] == rank[j]) rank[i]++;
    }
    else {
        parent[i] = j;
    }
}

