bool isGood(int s,int &sum,vector<int> &arr)
{
    if(s==sum) return false;
    int temp = 0;
    for(int i=0;i<arr.size();i++)
    {
        temp += arr[i];
        if(temp==s) temp=0;
        else if(temp>s) return false;
    }
    return temp==0;
}

