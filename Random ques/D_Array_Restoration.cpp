void buildTree(vector<int> &arr,vector<int> &segTree,int l,int r,int ind)
{
    if(l==r)
    {
        segTree[ind] = arr[l];
        return;
    }
    int mid = l + (r-l)/2;
    buildTree(arr,segTree,l,mid,2*ind+1);
    buildTree(arr,segTree,mid+1,r,2*ind+2);
    auto left = segTree[2*ind+1];
    auto right = segTree[2*ind+2];
    segTree[ind] = min(left,right);
}

int query(vector<int> &segTree,int l,int r,int ind,int s,int e)
{
    if(l>=s && r<=e) return segTree[ind];
    if(l>e || r<s) return INT32_MAX;
    int mid = l + (r-l)/2;
    auto left = query(segTree,l,mid,2*ind+1,s,e);
    auto right = query(segTree,mid+1,r,2*ind+2,s,e);
    return min(left,right);
}

