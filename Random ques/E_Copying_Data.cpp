    vector<ll> a(n);
    vector<ll> b(n);
    vector<int> segTree(4*n,n);
    for(int i=0;i<n;i++)
    {
        cin>>a[i];
    }
    for(int i=0;i<n;i++)
    {
        cin>>b[i];
    }
