void update(vector<int> &segTree,int &n,int l,int r,int ind,int s,int e,int &x)
{
    if(l!=r && segTree[ind]<n)
    {
        segTree[2*ind+1] = segTree[ind];
        segTree[2*ind+2] = segTree[ind];
        segTree[ind] = n;
    }
    if(l>=s && r<=e)
    {
        segTree[ind] = x;
        return;
    }
    if(l>e || r<s) return;
    int mid = l + (r-l)/2;
    update(segTree,n,l,mid,2*ind+1,s,e,x);
    update(segTree,n,mid+1,r,2*ind+2,s,e,x);
}

int query(vector<int> &segTree,int &n,int l,int r,int ind,int &i)
{
    if(l!=r && segTree[ind]<n)
    {
        segTree[2*ind+1] = segTree[ind];
        segTree[2*ind+2] = segTree[ind];
        segTree[ind] = n;
    }
    if(l==r)
    {
        return segTree[ind];
    }
    int mid = l + (r-l)/2;
    if(i<=mid)
    {
        return query(segTree,n,l,mid,2*ind+1,i);
    }
    else
    {
        return query(segTree,n,mid+1,r,2*ind+2,i);
    }
}

