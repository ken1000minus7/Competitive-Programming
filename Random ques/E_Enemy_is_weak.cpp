void buildTree(vector<ll> &arr,vector<vector<ll>> &segTree,int l,int r,int ind)
{
    if(l==r)
    {
        segTree[ind] = {arr[l]};
        return;
    }
    int mid = l + (r-l)/2;
    buildTree(arr,segTree,l,mid,2*ind+1);
    buildTree(arr,segTree,mid+1,r,2*ind+2);
    auto left = segTree[2*ind+1];
    auto right = segTree[2*ind+2];
    int i=0,j=0;
    vector<ll> ans;
    while(i<left.size() || j<right.size())
    {
        if(j>=right.size() || (i<left.size() && left[i]<right[j]))
        {
            ans.push_back(left[i]);
            i++;
        }
        else
        {
            ans.push_back(right[j]);
            j++;
        }
    }
    segTree[ind] = ans;
}
 
pair<int,int> query(vector<vector<ll>> &segTree,int l,int r,int ind,int s,int e,ll &x)
{
    if(l>=s && r<=e)
    {
        int left = 0,right = segTree[ind].size()-1;
        int ans=segTree[ind].size();
        while(left<=right)
        {
            int mid = left + (right-left)/2;
            if(segTree[ind][mid]>=x)
            {
                ans = mid;
                right = mid - 1;
            }
            else left = mid+1;
        }
        int small = ans;
        int great = segTree[ind].size()-ans;
        return {great,small};
    }
    if(l>e || r<s) return {0,0};
    int mid = l + (r-l)/2;
    auto p1 = query(segTree,l,mid,2*ind+1,s,e,x);
    auto p2 = query(segTree,mid+1,r,2*ind+2,s,e,x);
    return {p1.first+p2.first,p1.second+p2.second};
}

