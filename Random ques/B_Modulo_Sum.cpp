bool isPoss(int i,int rem,vector<ll> &arr,vector<vector<int>> &dp,int &m)
{
    if(dp[i][rem]!=-1) return dp[i][rem];
    if(arr[i]%m==rem)
    {
        return dp[i][rem] = 1;
    }
    int myrem = arr[i]%m;
    int nrem = rem - myrem;
    if(nrem<0) nrem += m;
    dp[i][rem] = 0;
    for(int j=i+1;j<arr.size();j++)
    {
        if(isPoss(j,nrem,arr,dp,m))
        {
            dp[i][rem] = 1;
            break;
        }
    }
    return dp[i][rem];
}

