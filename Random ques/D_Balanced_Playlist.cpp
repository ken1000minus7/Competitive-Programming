void buildMax(vector<ll> &arr,vector<ll> &segTree,int l,int r,int ind)
{
    if(l==r)
    {
        segTree[ind] = arr[l];
        return;
    }
    int mid = l + (r-l)/2;
    buildMax(arr,segTree,l,mid,2*ind+1);
    buildMax(arr,segTree,mid+1,r,2*ind+2);
    ll left = segTree[2*ind+1];
    ll right = segTree[2*ind+2];
    segTree[ind] = max(left,right);
}

ll queryMax(vector<ll> &segTree,int l,int r,int ind,int s,int e)
{
    if(l>=s && r<=e) return segTree[ind];
    if(l>e || r<s) return INT32_MIN;
    int mid = l + (r-l)/2;
    ll left = queryMax(segTree,l,mid,2*ind+1,s,e);
    ll right = queryMax(segTree,mid+1,r,2*ind+2,s,e);
    return max(left,right);
}

void buildMin(vector<ll> &arr,vector<ll> &segTree,int l,int r,int ind)
{
    if(l==r)
    {
        segTree[ind] = arr[l];
        return;
    }
    int mid = l + (r-l)/2;
    buildMin(arr,segTree,l,mid,2*ind+1);
    buildMin(arr,segTree,mid+1,r,2*ind+2);
    ll left = segTree[2*ind+1];
    ll right = segTree[2*ind+2];
    segTree[ind] = min(left,right);
}

ll queryMin(vector<ll> &segTree,int l,int r,int ind,int s,int e)
{
    if(l>=s && r<=e) return segTree[ind];
    if(l>e || r<s) return INT32_MAX;
    int mid = l + (r-l)/2;
    ll left = queryMin(segTree,l,mid,2*ind+1,s,e);
    ll right = queryMin(segTree,mid+1,r,2*ind+2,s,e);
    return min(left,right);
}

int getAns(int i,int &n,vector<int> &dp,vector<ll> &arr,vector<ll> &segMax,vector<ll> &segMin)
{
    if(dp[i]!=-1) return dp[i];
    int l=i+1,r=3*n-1;
    int maxi = 3*n;
    while(l<=r)
    {
        int mid = l + (r-l)/2;
        ll rangeMaxi = queryMax(segMax,0,3*n-1,0,i+1,mid);
        if(rangeMaxi>=arr[i])
        {
            maxi = mid;
            r = mid - 1;
        }
        else l = mid + 1;
    }
    l=i+1,r=3*n-1;
    int mini = 3*n;
    while(l<=r)
    {
        int mid = l + (r-l)/2;
        ll rangeMini = queryMin(segMin,0,3*n-1,0,i+1,mid);
        if(rangeMini*2<arr[i])
        {
            mini = mid;
            r = mid - 1;
        }
        else l = mid + 1;
    }
    if(maxi<mini)
    {
        return dp[i]=getAns(maxi,n,dp,arr,segMax,segMin) + maxi-i;
    }
    else 
    {
        return dp[i]=mini-i;
    }
}
