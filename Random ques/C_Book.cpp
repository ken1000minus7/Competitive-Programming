    int t;
    cin >> t;
    while(t--) {
        int n;
        cin >> n;
        vector<vector<int>> adj(n, vector<int>());
        vector<int> indegree(n);
        set<int> s;
        int start = -1;
        for(int i = 0; i < n; i++) {
            int k;
            cin >> k;
            if(k == 0) s.insert(i);
            while(k--) {
                int a;
                cin >> a;
                a--;
                indegree[i]++;
                adj[a].push_back(i);
            }
        }
        vector<int> topo;
        bool op = true;
        int prev = -1;
        while(!s.empty()) {
            auto it = s.upper_bound(prev);
            int i = it == s.end() ? *s.begin() : *it;
            s.erase(i);
            topo.push_back(i);
            for(int j : adj[i]) {
                if(indegree[j] == 0) {
                    op = false;
                    break;
                }
                indegree[j]--;
                if(indegree[j] == 0) {
                    s.insert(j);
                }
            }
            prev = i;
        }
        if(!op || topo.size() != n) {
            cout << -1 << "\n";
            continue;
        }
        int ans = 1;
        for(int i = 0; i < n - 1; i++) {
            if(topo[i] > topo[i + 1]) ans++;
        }
        // for(int a : topo) {
        //     cout << a << " ";
        // }
        // cout << "\n";
        cout << ans << "\n";
    }
    return 0;
